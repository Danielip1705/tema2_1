package ejerciciosfor;

public class Ejercicio3 {

	public static void main(String[] args) {
		//con for, indicamos con la variable int el multiplo(7)
		//si multiplo es menor o igual a 100 se suma 7
		
		for (int multiplo = 7; multiplo<=100;multiplo +=7){
			
			//imprimimos en pantalla el resultado(multiplo)
			System.out.println(multiplo);
		}
	}

}
